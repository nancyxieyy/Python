# int
a = 1
print(a)
print(type(a))

# float
b = 1.5
print(b)
print(type(b))

# boolean
c = True
print(c)
print(type(c))

# string
d = 'China'
print(d)
print(type(d))

# list
e = [1, 2, 3, 4]
print(e)
print(type(e))

# tuple
f = (1, 2, 3, 4, 5)
print(f)
print(type(f))

# dict
g = {'name':'zhangsan', 'age':18}
print(g)
print(type(g))